package com.training.java.core.typecasting;

public class TypeCastingDemo {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		
		/*
		int age = 20;
		
		long bigAge = age;
		*/
		
		long age = 20;
		
		int smallAge = (int) age;
		
		System.out.println("smallAge = " + smallAge);
		
		
	}

}
